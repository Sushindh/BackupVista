export const checkDemoLogin = (email, password) => {
  // Check admin
  if (
    email === DEMO_USERS.admin.emailId &&
    password === DEMO_USERS.admin.password
  ) {
    return { success: true, user: DEMO_USERS.admin, token: "demo-admin-token" };
  }
  // Check coordinator
  if (
    email === DEMO_USERS.coordinator.emailId &&
    password === DEMO_USERS.coordinator.password
  ) {
    return {
      success: true,
      user: DEMO_USERS.coordinator,
      token: "demo-coordinator-token",
    };
  }
  // Check locked coordinator
  if (
    email === DEMO_USERS.locked_coordinator.emailId &&
    password === DEMO_USERS.locked_coordinator.password
  ) {
    return {
      success: true,
      user: DEMO_USERS.locked_coordinator,
      token: "demo-locked-token",
    };
  }
  // Check faculty
  if (
    email === DEMO_USERS.faculty.emailId &&
    password === DEMO_USERS.faculty.password
  ) {
    return {
      success: true,
      user: DEMO_USERS.faculty,
      token: "demo-faculty-token",
    };
  }
  return { success: false };
};

